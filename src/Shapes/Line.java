package Shapes;

/**
 *
 * @author ajanuser
 */
public class Line {
    
    public double xstart=0;
    public double ystart=0;
    public double xend=0;
    public double yend=0;

    public Line(int x_start,int y_start,int x_end,int y_end) {
        this.xstart = x_start;
        this.ystart=y_start;
        this.xend=x_end;
        this.yend=y_end;
    }
    
}
